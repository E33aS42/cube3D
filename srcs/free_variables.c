#include "../include/cub3d.h"

void	free_text_spr(char *s)
{
	if (s)
	{
		free(s);
		s = NULL;
	}
}

void	free_int(int *d)
{
	if (d)
		free(d);
}

void	free_double(double *d)
{
	if (d)
		free(d);
}

void	free_tab(char **tab)
{
	int	i;

	i = 0;
	if (tab)
	{
		while (tab[i])
		{
			free(tab[i]);
			tab[i] = NULL;
			i++;
		}
		free(tab);
	}
}
